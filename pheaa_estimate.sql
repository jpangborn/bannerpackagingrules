-- Sequence 1: EFC <= 4000

SELECT CASE 
  WHEN ((32000 - (RPTNEED_EFC_AMT + NVL(RPKALGR.F_GET_FUND_OFFER(:aidy, :pidm, 'PELL', RPTNEED_SIMULATE_SW),0))) * .47) > 4700 
  THEN FLOOR(ROUND(4700 * .8493)/2)*2
  WHEN ((32000 - (RPTNEED_EFC_AMT + NVL(RPKALGR.F_GET_FUND_OFFER(:aidy, :pidm, 'PELL', RPTNEED_SIMULATE_SW),0))) * .47) < 500 
  THEN 0 
  WHEN ROUND((FLOOR(((32000 - (RPTNEED_EFC_AMT + NVL(RPKALGR.F_GET_FUND_OFFER(:aidy, :pidm, 'PELL', RPTNEED_SIMULATE_SW),0))) * .47)/50)*50) * .8493) < 500
  THEN 500
  ELSE FLOOR(ROUND((FLOOR(((32000 - (RPTNEED_EFC_AMT + NVL(RPKALGR.F_GET_FUND_OFFER(:aidy, :pidm, 'PELL', RPTNEED_SIMULATE_SW),0))) * .47)/50)*50) * .8493)/2)*2 END
FROM RPTNEED
WHERE RPTNEED_EFC_AMT <= 4000
  AND RPTNEED_AIDY_CODE = :aidy
  AND RPTNEED_PIDM = :pidm

